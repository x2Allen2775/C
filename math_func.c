#include<stdio.h>
#include<math.h>
int main ()
{
    int x=9;
    x=sqrt(x);
    //this gives the sqrt of the vartibale inside its bracket
    x=pow(x,4);
    //this gives x power 4
    x=round(x);
    //this gives the rounded value of the variable inside it
    x=ceil(x);
    //this gives the rounded up value of the variable in it
    x=floor(x);
    //this gives the rounded down value of the variable inside the bracket
    x=abs(x);
    //this gives the absolute vale of the value inside the bracket
    x=log(x);
    //this gies the natural log value of the variable inside the bracket
    x=sin(x);
    //this gies the value of sin whatever it is inside the bracket and the x should be in radians
    //similarly
    x=cos(x);
    x=tan(x);
    
return 0;
}